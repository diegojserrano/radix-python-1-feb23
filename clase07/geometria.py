from math import pi

def perimetro_cuadrado(lado):
    return lado * 4

def superficie_cuadrado(lado):
    return lado ** 2

def perimetro_rectangulo(base, altura):
    return (base + altura) * 2

def superficie_rectangulo(base, altura):
    return base * altura

def cuadrado(x):
    return x ** 2

def superficie_circulo(radio):
    return pi * cuadrado(radio)
